open Errors

module OpenFile = struct

  let in_bin (filename : string) : in_channel =
    try
      let c = open_in_bin filename in
      (* This should raise an exception if a directory was opened *)
      let (_:int) = in_channel_length c in
      c
    with Sys_error s ->
      raise (Errors.FileError (filename, s))

end

